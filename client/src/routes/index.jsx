import { createBrowserRouter } from "react-router-dom";

import { Login, Register, Layout, Home, Request } from "../Pages/index";

const router = createBrowserRouter([
    {
        path: "/",
        element: <Layout />,
        children: [
            {
                path: "/",
                element: <Home />,
            },
            {
                path: "/request",
                element: <Request />,
            },
        ],
    },
    {
        path: "/register",
        element: <Register />,
    },
    {
        path: "/login",
        element: <Login />,
    },
]);

export default router;